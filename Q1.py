# Q1: Demonstrate int, float and type conversion


num1 = 10          # int
num2 = 5.5         # float

print("Integer value:", num1, "Type:", type(num1))
print("Float value:", num2, "Type:", type(num2))


converted_float = float(num1)
converted_int = int(num2)

print("Converted int to float:", converted_float, "Type:", type(converted_float))
print("Converted float to int:", converted_int, "Type:", type(converted_int))
